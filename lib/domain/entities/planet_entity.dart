class PlanetEntity {

  final dynamic name;
  final dynamic rotation_period;
  final dynamic orbital_period;
  final dynamic diameter;
  final dynamic climate;
  final dynamic gravity;
  final dynamic terrain;
  final dynamic surface_water;
  final dynamic population;
  final dynamic residents;
  final dynamic films;
  final dynamic url;

  PlanetEntity({
    required this.name,
    required this.rotation_period,
    required this.orbital_period,
    required this.diameter,
    required this.climate,
    required this.gravity,
    required this.terrain,
    required this.surface_water,
    required this.population,
    required this.residents,
    required this.films,
    required this.url
  });
}